#!/usr/bin/env node
// scripts/generate-master-migration.js
// Generates DB/migrations/000_MASTER_MIGRATION.sql by concatenating the canonical migration sequence.

const fs = require('fs');
const path = require('path');

const MIGRATIONS_DIR = path.join(__dirname, '..', 'DB', 'migrations');

const FILES = [
  '000_FIX_credit_ledger_delta_credits.sql',
  '000_COMPLETE_CONSOLIDATED_SCHEMA.sql',
  '000_COMPLETE_VIEWS_PATCH.sql',
  '019_payout_reconciliation_flags.sql',
  '057_pt_safety_reports.sql',
  '058_gamification_frontend_spec_tables.sql',
  '059_add_invoice_status_and_invoices.sql',
  '060_add_reviews_ai_worker_stripe_tables.sql',
  '061_add_cleaner_id_payout_misc_tables.sql',
];

const HEADER = `-- ============================================================
-- PURETASK MASTER MIGRATION - SINGLE FILE (ALL CANONICAL MIGRATIONS)
-- ============================================================
-- Generated by: npm run db:generate:master-migration
-- Combines in order:
--   000_FIX_credit_ledger_delta_credits.sql
--   000_COMPLETE_CONSOLIDATED_SCHEMA.sql (001-056 + 901-906)
--   000_COMPLETE_VIEWS_PATCH.sql (views + cleaner_tooltip_interactions missing from COMPLETE)
--   019_payout_reconciliation_flags.sql (table referenced by COMPLETE FK)
--   057_pt_safety_reports.sql
--   058_gamification_frontend_spec_tables.sql
--   059_add_invoice_status_and_invoices.sql
--   060_add_reviews_ai_worker_stripe_tables.sql
--   061_add_cleaner_id_payout_misc_tables.sql
-- ============================================================
-- Run on a FRESH database for full schema, or on existing (idempotent).
-- See docs/active/MASTER_MIGRATIONS.md for canonical order.
-- ============================================================

`;

const outPath = path.join(MIGRATIONS_DIR, '000_MASTER_MIGRATION.sql');
const parts = [HEADER];

let totalChars = 0;
for (const file of FILES) {
  const fullPath = path.join(MIGRATIONS_DIR, file);
  if (!fs.existsSync(fullPath)) {
    console.error('Missing:', fullPath);
    process.exit(1);
  }
  const content = fs.readFileSync(fullPath, 'utf8');
  if (!content || content.trim().length === 0) {
    console.error('Empty or whitespace-only file:', file);
    process.exit(1);
  }
  totalChars += content.length;
  parts.push('-- ########## INLINED: ', file, ' ##########\n');
  parts.push(content);
  if (!content.endsWith('\n')) parts.push('\n');
  parts.push('\n');
}

const outContent = parts.join('');
fs.writeFileSync(outPath, outContent, 'utf8');
console.log('Wrote', outPath);
console.log('Files inlined:', FILES.length);
console.log('Total length:', outContent.length, 'chars');
console.log('Source total:', totalChars, 'chars');
